//Arithimatic Operator
public class ArithimaticOperator{
public static void main(String args[]){
	int value1=40;
	int value2=15;
	System.out.println("sum="+(value1+value2));
	System.out.println("difference="+(value1-value2));
	System.out.println("mul="+(value1*value2));
	System.out.println("div="+(value1/value2));
	System.out.println("mod="+(value1%value2));
	

	double value3=40;
	double value4=15;
	System.out.println("sum="+(value3+value4));
	System.out.println("difference="+(value3-value4));
	System.out.println("mul="+(value3*value4));
	System.out.println("div="+(value3/value4));
	System.out.println("mod="+(value3%value4));

}
}